    public static boolean isJustEmoji(String text){
        if(text == null) return false;
        boolean hasEmoji = false;
        int size = text.length();
        for (int i = 0; i < size ;) {
            int skip = 1;
            hasEmoji = false;
            int unicode;
            unicode = Character.codePointAt(text, i);
            skip = Character.charCount(unicode);

            if(Character.isSpaceChar(unicode)) {
                i += skip;
                continue;
            }

            if (unicode < 0x7d0 ) {//unicodes beloww 2000
                return false;
            }

            if (singleMap.containsKey(unicode)) {
                hasEmoji = true;
            } else if (isMultiMap.containsKey(unicode)) {
                if (i + skip < size) {
                    int code1 = Character.codePointAt(text, i);//same as : unicode
                    int charSize = Character.charCount(code1);//don't use skip -- can be bugy
                    int code2 = Character.codePointAt(text, i + charSize);
                    String val = fromMany(code1, code2);
                    Integer multiCharIcon = multiMap.get(val);
                    if (multiCharIcon != null) {
                        hasEmoji = true;
                    }
                }
            }

            if (hasEmoji == false) {// &&  ((i+ skip) < textLengthToProcess) ) {
                return false;
            }
            i += skip;
        }
        return true;
    }

    public static String fromMany(int codePoint1, int codePoint2) {
        String str ="";
        if(codePoint1< 0xffff){
            str += Character.toString((char) codePoint1);
        }else {
            str += newString(codePoint1);
        }

        if(codePoint2< 0xffff){
            str += Character.toString((char) codePoint2);
        }else {
            str += newString(codePoint2);
        }

        return str;
    }

    public static final String newString(int codePoint) {
        if (Character.charCount(codePoint) == 1) {
            return String.valueOf(codePoint);
        } else {
            return new String(Character.toChars(codePoint));
        }
    }

